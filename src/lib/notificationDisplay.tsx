/**
 * All notification types emitted by the DB triggers / RPCs.
 * Keep in sync with the `type` values used in:
 *   - supabase/migrations/add_notifications_system.sql
 *   - supabase/migrations/add_join_requests_system.sql
 */
export const NOTIFICATION_TYPES = [
  "score_submitted",
  "score_approved",
  "match_completed",
  "member_joined",
  "match_scheduled",
  "join_request",
  "join_approved",
  "join_rejected",
] as const

export type NotificationType = (typeof NOTIFICATION_TYPES)[number]

/** Narrow an unknown string (from the DB) to a known NotificationType. */
export function asNotificationType(t: string): NotificationType | undefined {
  return (NOTIFICATION_TYPES as readonly string[]).includes(t)
    ? (t as NotificationType)
    : undefined
}

